print("Input/Output e conversão de tipos")
# Inputs
A = input() # esse input vai esperar que algo seja digitado para ser armazenado na variável A
inteiro = input("Digite um número inteiro: ") # esse input vai esperar que um inteiro seja digitado para ser armazenado na variável inteiro
